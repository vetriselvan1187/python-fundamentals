

# for loop

for i in range(1, 10):
    print(i)

print('--------')
list = [1, 2, 3, 4, 5, 6]

for num in list:
    print(num)

print('--------')
for index in range(len(list)):
    print(list[index])



# while loop

i = 0
while i < 10:
    print(i)
    i = i+1

print('---------')

list = [1, 2, 3, 4, 5, 6]

i = 0
while i < len(list):
    print(list[i])
    i = i+1


