
# datatypes

x = 5 
print(type(x))

x = 'name'
print(type(x))

x = 12.45
print(type(x))

x = True
print(type(x))

list = [1,2,3,4]
print(type(list))

t = (1,2,3,4)
print(type(t))

print(type(range(5)))

s = {1, 2, 3, 4, 5}
print(type(s))

fset = frozenset(s)
print(type(fset))

employee = {'id' : 1, 'name':'MyName', 'age':22}
print(type(employee))

bite = b'1234'
print(type(bite))

print(type(None)) # None type

bar = bytearray(bite)
print(type(bar))

m = memoryview(bar)
print(type(m))





